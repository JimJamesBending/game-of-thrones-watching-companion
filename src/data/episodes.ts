export function getEpisodeContent(id: string): EpisodeContent {
  if (curatedEpisodes[id]) {
    return curatedEpisodes[id]
  }

  const fallback = episodeIndex.find((item) => item.id === id) ?? episodeIndex[0]

  return {
    ...fallback,
    status: 'scaffold',
    spoilerBoundary: `Route scaffold for Season ${fallback.season}, Episode ${fallback.episode}. Add only information known by the end of this episode.`,
    summary:
      'This episode page is ready for spoiler-safe curation. The typed content layer keeps names, links, houses, and places separate from the UI.',
    houses: [],
    characters: [],
    relationships: [],
    locations: [],
    routes: [],
    sources: [],
  }
}
